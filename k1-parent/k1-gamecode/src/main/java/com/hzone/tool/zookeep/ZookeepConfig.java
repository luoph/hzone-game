package com.hzone.tool.zookeep;

/**
 * zookeep配置
 * @author zehong.he
 *
 */
public class ZookeepConfig {
	private String ipPort;
	private String user;
	private String pwd;
	
	public String getIpPort() {
		return ipPort;
	}
	public void setIpPort(String ipPort) {
		this.ipPort = ipPort;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
}
